    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(jComboBox1.getSelectedItem() == null){
            JOptionPane.showMessageDialog(null, "Errorx: No hay reservas hechas", "InfoBox", JOptionPane.INFORMATION_MESSAGE);
        }else{
            if(jFormattedTextField1.getText().isEmpty() == true || jTextField1.getText().isEmpty() == true){
                JOptionPane.showMessageDialog(null, "Errorx: Rellene todos los campos, por favor", "InfoBox", JOptionPane.INFORMATION_MESSAGE);
            }else{
                String Hora_inicio = jFormattedTextField1.getText();
                String Observacion = jTextField1.getText();
                String dni = Integer.toString(Login.guarda.getDni());
                String Cod_reserva = (String) jComboBox1.getSelectedItem();
                if(validarHora(Hora_inicio)){
                    reserva = new Reserva(Cod_reserva,Observacion,Hora_inicio,dni,llego);
                    Login.guarda.RegistrarLLegada(reserva);
                }else{
                    JOptionPane.showMessageDialog(null, "Errorx:hora invalida ", "InfoBox" , JOptionPane.INFORMATION_MESSAGE);
                }

                jFormattedTextField1.setText("");
                jTextField1.setText("");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

