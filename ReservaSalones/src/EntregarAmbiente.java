     private boolean validarHora(String hora){
        boolean b;
        char[] a = hora.toString().toCharArray();
        String[] c = hora.split(":");
        if ((a[0] == ' ') || (a[1] == ' ') || (a[2] == ' ') 
                || (a[3] == ' ') || (a[4] == ' ') 
                || (getInt(c[0]) > 24) || (getInt(c[1]) > 59)) {
            b=false;
        }else{
            b=true;
        }
        return b;
    }
    private int getInt(String valor){
        int integer = Integer.parseInt(valor);
        return integer;
    }
